<template>
  <div
    class="container-fluid"
    style="
      width: 100vw;
      position: sticky;
      margin: 0;
      padding: 0;
      align-items: center;
      text-align: center;
    "
  >
    <nav
      class="navbar navbar-expand-lg bg-white sticky-top navbar-light shadow-lg"
      style="
        height: 4em;
        margin-left: 0;
        margin-right: 0;
        align-items: center;
        text-align: center;
      "
    >
      <NavBar
        :cart="cart"
        :total="total"
        :user="user"
        :profile="profile"
        :favorites="favorites"
      />
    </nav>
    <div class="container-fluid mt-5" style="margin-left: 10%">
      <div
        class="row"
        style="
          display: -ms-flexbox;
          display: flex;
          -ms-flex-wrap: wrap;
          flex-wrap: wrap;
          margin: 0 -16px;
        "
      >
        <div class="col-md-8" style="max-width: 1200px; margin-left: 3%">
          <h3 class="text-center mb-4" style="margin-left: 12%">
            <i class="fa fa-heart red-color" style="font-size: 1.6rem"> </i>
            Favorite Products
          </h3>
          <table class="table table-hover">
            <tbody>
              <tr v-for="product in favorites" :key="product.id">
                <td style="padding-top: 1%">
                  <img
                    :src="`${backendEndpoint}/static/img/${product.username}/${product.name}/${product.image}`"
                    class="img-fluid"
                    alt="Product Image"
                    style="max-width: 270px; height: 200px; object-fit: cover"
                  />
                </td>
                <td
                  style="cursor: pointer; padding-top: 1%"
                  @click="redirectToItemFromCart(product.id)"
                >
                  <h6>{{ truncateName(product.name, 45) }}</h6>
                  <p
                    style="
                      font-size: 0.95em;
                      padding-top: 3%;
                      padding-right: 10%;
                      padding-left: 10%;
                      padding-bottom: 0;
                    "
                  >
                    {{ truncateName(product.description, 200) }}
                  </p>
                  <p style="cursor: pointer">
                    <i>
                      <span
                        v-for="i in 5"
                        :key="i"
                        :class="getStarClasses(i, product.rating_float)"
                      ></span>
                      <span
                        :id="'overall-rating' + product.id + '-float'"
                        class="overall-rating"
                        >&nbsp;{{ product.rating_float }}</span
                      >
                    </i>
                    <span
                      :id="'overall-rating' + product.id"
                      class="overall-rating2"
                    >
                      ({{ product.reviewNumber }})
                    </span>
                  </p>
                </td>
                <td style="padding-top: 5.3%; padding-right: 10px">
                  <b
                    >${{
                      (
                        product.price -
                        product.price * product.discount
                      ).toFixed(2)
                    }}</b
                  >
                </td>
                <td style="padding: 0; padding-top: 5%">
                  <button
                    type="button"
                    ref="addToCartButton"
                    @click="addToCart(product)"
                    class="btn btn-outline-primary btn-sm m-0"
                  >
                    <i class="bi bi-cart-fill"></i>
                  </button>
                </td>
                <td style="padding: 0.15em; padding-top: 5%">
                  <button
                    type="button"
                    class="btn btn-outline-danger btn-sm m-0"
                    @click="removeFromFavorites(product.id)"
                  >
                    <i class="bi bi-trash"></i>
                  </button>
                </td>
              </tr>
            </tbody>
          </table>
        </div>
      </div>
    </div>
    <div
      class="toast"
      id="cartToast"
      role="alert"
      aria-live="assertive"
      aria-atomic="true"
      data-bs-autohide="false"
      style="
        position: fixed;
        top: 12%;
        right: 5%;
        transform: translate(0, -50%);
        width: 250px;
        z-index: 1000;
      "
    >
      <div
        class="toast-body"
        id="cartToastBody"
        style="font-weight: 900; font: 1.1em"
      ></div>
    </div>
  </div>
</template>

<script>
import NavBar from '../components/MyNavbar.vue'
import errorHandlingMixin from '../errorHandlingMixin'

export default {
  components: {
    NavBar
  },
  props: ['profile'],
  data() {
    return {
      item: null,
      itemId: this.itemId,
      backendEndpoint: 'https://fast3-backend.azurewebsites.net'
    }
  },
  mixins: [errorHandlingMixin],
  created() {
    this.$store.dispatch('initializeUser').catch(this.handleError)
    this.$store.dispatch('readFromCartVue').then(() => {
      const fetchRatingsPromises = this.$store.state.favorites.map(product => {
        return this.$store.dispatch('getItemRating', product.id)
      })
      return Promise.all(fetchRatingsPromises)
    })
  },
  computed: {
    total() {
      return this.$store.getters.total
    },
    user() {
      return this.$store.getters.user
    },
    accessToken() {
      return this.$store.state.accessToken
    },
    cart() {
      return this.$store.state.cart
    },
    favorites() {
      return this.$store.state.favorites
    }
  },
  methods: {
    getStarClasses(index, rating) {
      const filledStars = Math.floor(rating)
      if (index <= filledStars) {
        return 'fa fa-star checked'
      } else if (index === filledStars + 1 && rating % 1 !== 0) {
        return 'fa fa-star-half-full checked'
      } else {
        return 'fa fa-star-o checked'
      }
    },
    getRatingItemCount(rating) {
      const items = this.$store.state.products // Assuming products are stored in the store
      const count = items.reduce((accumulator, item) => {
        const floatRating = parseFloat(item.rating_float)
        const roundedRating = Math.floor(floatRating + 0.5) // Round to the nearest integer
        if (roundedRating === rating) {
          return accumulator + 1
        }
        return accumulator
      }, 0)
      return count
    },
    itemAlreadyInCart(product) {
      return this.cart.some(item => item.id === product.id)
    },
    addToCart(product) {
      this.$store.dispatch('addToCart', product)
    },
    removeFromFavorites(itemId) {
      this.$store.dispatch('removeFromFavorites', itemId)
    },
    redirectToItemFromCart(itemId) {
      this.$store.dispatch('redirectToItem', itemId)
    },
    updateQuantity(product_id, newQuantity) {
      this.$store.dispatch('UpdateItemQuantity', {
        product_id,
        newQuantity
      })
    },
    truncateName(description, maxLength) {
      if (description.length > maxLength) {
        return description.substring(0, maxLength) + '..'
      }
      return description
    }
  }
}
</script>
